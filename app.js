const calculator = {
    add:function(a, b){
        console.log(a+b);
    },
    mon:function(a, b){
        console.log(a-b);
    },
    div:function(a, b){
        console.log(a/b);
    },
    powerOf:function(a, b){
        console.log(a**b);
    }
}

calculator.add(2, 4);
calculator.mon(2, 4);
calculator.div(2, 4);
calculator.powerOf(2, 4);
